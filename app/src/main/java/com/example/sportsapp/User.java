package com.example.sportsapp;

public class User {
    private String username;
    private String password;

    public void setUsername(String value) {
        username = value;
    }

    public String getUsername() {
        return username;
    }

    public void setPassword(String value) {
        password = value;
    }

    public String getPassword() {
        return password;
    }
}
